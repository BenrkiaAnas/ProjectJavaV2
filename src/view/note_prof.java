    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        String name_cour = jComboBox1.getSelectedItem().toString();
        String etudiant = jComboBox2.getSelectedItem().toString();
        Float note =  Float.parseFloat(jTextField3.getText());
        
        CourProfDao courDAO = new CourProfDao();
        courDAO.seConnecter();
        Cour cour = courDAO.getCourByName(name_cour);
        
        PersonneDao personneDao = new PersonneDao();
        personneDao.seConnecter();
        Personne personne = personneDao.getPersonneByName(etudiant);
        
        ParticipationDao participationDao = new ParticipationDao();
        participationDao.seConnecter();
        
        Participation participation = new Participation(cour, personne);
        
        participationDao.updateNote(participation,note);
        
        JOptionPane.showMessageDialog(this, "Vous avez changer la note d'étudiant avec succés","Confirmation",JOptionPane.INFORMATION_MESSAGE);
        
        getInfoToTable();
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

