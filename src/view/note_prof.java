            CourProfDao courdao = new CourProfDao();
            courdao.seConnecter();

            Cour cour = courdao.getCourByName(cour_name);

            PersonneDao personneDao = new PersonneDao();
            personneDao.seConnecter();
            
            ArrayList<Personne> etudiants = personneDao.getEtudiantsByCour(cour);

